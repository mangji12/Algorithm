# 문제 6
# 문자열을 입력받고, 문자열에서 개별 문자가 나오는 횟수를 출력하세요.

# 단, count() 메서드는 사용하지마세요.

'''
출력 예시 1
문자열을 입력하세요 > hello # 사용자 입력
h 1
e 1
l 2
o 1
출력 예시 2
문자열을 입력하세요 > else # 사용자 입력
e 2
l 1
s 1
'''

string = input('문자열을 입력하세요 > ')
dict_var = {}
for char in string:
    if char in dict_var.keys():
        dict_var[char] += 1
    elif char not in dict_var.keys():
        dict_var[char] = 1 # (반복되는) char의 key가 같지 않으면 dict_var[char](밸류값)은 = 1(True) 

for key, value in dict_var.items():
    print(key,value)



